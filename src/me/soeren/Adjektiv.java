package me.soeren;

public class Adjektiv {
    String nominativ;
    String firstGenitiv;
    String secondGenitiv;
    String thirdGenitiv;
    String bedeutung;

    public Adjektiv(String nominativ, String firstGenitiv, String secondGenitiv, String thirdGenitiv, String bedeutung){
        this.nominativ = nominativ;
        this.firstGenitiv = firstGenitiv;
        this.secondGenitiv = secondGenitiv;
        this.thirdGenitiv = thirdGenitiv;
        this.bedeutung = bedeutung;
    }
}
